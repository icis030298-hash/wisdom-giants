export const getProblemGreeting = (slug: string, problemId: string, locale: string = 'en'): string | null => {
  const list = (PROBLEM_GIANT_MAP as any)[problemId];
  if (!list) return null;
  const match = list.find((g: any) => g.slug === slug);
  if (!match || !match.greetings) return null;
  const l = (locale === 'ko' || locale === 'en' || locale === 'de' || locale === 'ja' || locale === 'es' || locale === 'fr' || locale === 'it' || locale === 'pt') ? locale : 'en';
  return match.greetings[l] || match.greetings['en'] || null;
};
