function fix(str) {
  let s = str;
  // Exact match translations
  for (let k in replacements) {
    if (s === k) return replacements[k];
  }

  // Substring replacements
  s = s.replace(/ 및 /g, ' και ');
  s = s.replace(/διπλω마τική/g, 'διπλωματική');
  s = s.replace(/Εκστρα테ίας/g, 'Εκστρατείας');
  s = s.replace(/Εκ스트ρα테ίας/g, 'Εκστρατείας');
  s = s.replace(/Δυνα태ας/g, 'Δυναστείας');
  s = s.replace(/κλί마κας/g, 'κλίμακας');
  s = s.replace(/ιε라πόστολο/g, 'ιεραπόστολο');
  s = s.replace(/Επέศρεψε/g, 'Επέστρεψε');
  s = s.replace(/Έγिने/g, 'Έγινε');
  s = s.replace(/ισلاμική/g, 'ισλαμική');
  s = s.replace(/διπλω마/g, 'διπλωμα');
  s = s.replace(/εκπαιδευση/g, 'εκπαίδευση');
  s = s.replace(/Μπάν테/g, 'Μπάντε');
  s = s.replace(/Ή대로/g, 'Ήθελε');

  return s;
}

function traverse(obj) {
  for (let k in obj) {
    if (typeof obj[k] === 'string') {
      obj[k] = fix(obj[k]);
    } else if (typeof obj[k] === 'object' && obj[k] !== null) {
      traverse(obj[k]);
    }
  }
}

